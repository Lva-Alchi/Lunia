/**
 * Transforms standard text into styled Unicode text based on the provided dictionary.
 * Characters not present in the dictionary (like punctuation or spaces) remain unchanged.
 * * @param {string} text - The input string to format.
 * @param {string} style - The requested style key (e.g., 'bold', 'monospace').
 * @returns {string} The transformed Unicode string.
 */
function fontStyle(text, style = 'bold') {
    if (typeof text !== 'string') return '';

    const selectedFont = fontMaps[style];
    
    if (!selectedFont) {
        console.warn(`[Font Service] Warning: Style '${style}' is not found.`);
        return text;
    }

    return text.split('').map(char => {
        return selectedFont[char] || char;
    }).join('');
}

/**
 * Scans the locales directory to get available languages.
 *
 * @param {string} localesDir - Path to locales folder (default: './locales').
 * @returns {Array<{code: string, name: string}>} List of available languages.
 */
function getAvailableLanguage(localesDir = '../locales') {
    const availableLanguage = [];
    const dirPath = path.resolve(__dirname, localesDir); // Get absolute path

    try {
        const files = fs.readdirSync(dirPath); // Read directory

        for (const file of files) {
            if (file.endsWith('.json')) { // Process JSON files only
                const langCode = file.replace('.json', ''); // Extract code (e.g., 'en')
                
                const filePath = path.join(dirPath, file);
                const fileContent = fs.readFileSync(filePath, 'utf-8');
                const parsedLocale = JSON.parse(fileContent); // Parse JSON

                // Add to list with fallback name
                availableLanguage.push({
                    code: langCode,
                    name: parsedLocale.langName || langCode.toUpperCase()
                });
            }
        }
    } catch (error) {
        console.error(`[i18n Error] Failed to read locales:`, error.message); // Handle errors
    }

    return availableLanguage;
}

